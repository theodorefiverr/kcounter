<template>
  <div>
    <canvas ref="canvas"  height="400"></canvas>
  </div>
</template>

<script lang="ts">
import { defineComponent } from 'vue';
import { Rive } from '@rive-app/canvas';

export default defineComponent({
  name: 'Rive',
  props: {
    src: String
  },
  mounted() {
    new Rive({
      canvas: this.$refs.canvas as HTMLCanvasElement,
      src: this.$props.src,
      autoplay: true
    })
  }
})
</script>